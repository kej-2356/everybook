package team1.project.vo;

public class Level {

	private String levelCode;
	private String levelName;
	private String levelPoint;
	private String rentPossibleCount;
	private String reservePossibleCount;
	private String levelRegDate;
	public String getLevelCode() {
		return levelCode;
	}
	public void setLevelCode(String levelCode) {
		this.levelCode = levelCode;
	}
	public String getLevelName() {
		return levelName;
	}
	public void setLevelName(String levelName) {
		this.levelName = levelName;
	}
	public String getLevelPoint() {
		return levelPoint;
	}
	public void setLevelPoint(String levelPoint) {
		this.levelPoint = levelPoint;
	}
	public String getRentPossibleCount() {
		return rentPossibleCount;
	}
	public void setRentPossibleCount(String rentPossibleCount) {
		this.rentPossibleCount = rentPossibleCount;
	}
	public String getReservePossibleCount() {
		return reservePossibleCount;
	}
	public void setReservePossibleCount(String reservePossibleCount) {
		this.reservePossibleCount = reservePossibleCount;
	}
	public String getLevelRegDate() {
		return levelRegDate;
	}
	public void setLevelRegDate(String levelRegDate) {
		this.levelRegDate = levelRegDate;
	}
	
	
}
